char *extract_cmd(char *path_and_cmd)
{
	int len;
	int i;
	char *cmd;

	cmd = NULL;	
	len = ft_strlen(path_and_cmd);

	if (path_and_cmd[len] == '/')
		return NULL;	

	i = len;
	while (path_and_cmd[i] != '/')
		i--;

	cmd = ft_strdup(&path_and_cmd[i + 1]);

	return cmd;	
}

