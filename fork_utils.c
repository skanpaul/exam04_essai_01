#include "microshell.h"

/* ************************************************************************** */
bool is_fork_error(int fork_code)
{
	if (fork_code == -1)
		return true;
	return false;
}

/* ************************************************************************** */
bool is_fork_child(int fork_code)
{
	if (fork_code == 0)
		return true;
	return false;
}

/* ************************************************************************** */
bool is_fork_parent(int fork_code)
{
	if (fork_code > 0)
		return true;
	return false;
}

